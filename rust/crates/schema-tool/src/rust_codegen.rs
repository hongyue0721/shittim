fn render_projected_decl(
    decl: &ProjectedDecl,
    names: &BTreeMap<RustDeclarationId, String>,
) -> Result<String> {
    match &decl.shape {
        ProjectedShape::Alias { target } => {
            render_type_alias(&decl.rust_name, &decl.doc_schema_id, target, names)
        }
        ProjectedShape::Object {
            member_plan,
            unknown_field_policy,
        } => render_struct(
            &decl.rust_name,
            &decl.doc_schema_id,
            member_plan,
            *unknown_field_policy,
            names,
        ),
        ProjectedShape::TaggedUnion {
            discriminator,
            variants,
        } => render_tagged_union(
            &decl.rust_name,
            &decl.doc_schema_id,
            discriminator,
            variants,
            names,
        ),
        ProjectedShape::StringEnum { values } => Ok(render_string_enum(
            &decl.rust_name,
            &decl.doc_schema_id,
            values,
        )),
        ProjectedShape::Const { value } => Ok(render_const_type(
            &decl.rust_name,
            &decl.doc_schema_id,
            value,
        )),
    }
}

fn render_type_alias(
    type_name: &str,
    schema_id: &str,
    target: &RustTypeExpr,
    names: &BTreeMap<RustDeclarationId, String>,
) -> Result<String> {
    let rendered = render_rust_type_expr(target, names, TypeRenderContext::TypesModule)?;
    Ok(format!(
        "/// Transparent root alias generated from `{schema_id}`\npub type {type_name} = {rendered};\n"
    ))
}

fn render_struct(
    type_name: &str,
    schema_id: &str,
    member_plan: &RustMemberPlan,
    unknown_field_policy: UnknownFieldPolicy,
    names: &BTreeMap<RustDeclarationId, String>,
) -> Result<String> {
    let serde_attribute = match unknown_field_policy {
        UnknownFieldPolicy::Forbid => "\n#[serde(deny_unknown_fields)]",
        UnknownFieldPolicy::Allow => "",
    };
    let mut body = format!(
        "/// Generated from `{schema_id}`\n#[derive(Debug, Clone, PartialEq, Serialize, Deserialize)]{serde_attribute}\npub struct {type_name} {{\n"
    );
    for field in &member_plan.declared {
        let rust_name = &field.rust_name;
        let mut rendered = render_rust_type_expr(&field.ty, names, TypeRenderContext::TypesModule)?;
        if field.presence == Presence::Optional && !rendered.starts_with("Option<") {
            rendered = format!("Option<{rendered}>");
        }

        let mut serde_attrs = Vec::new();
        if rust_name != &field.json_name {
            serde_attrs.push(format!("rename = {:?}", field.json_name));
        }
        if field.presence == Presence::Optional && field.nullability == Nullability::NonNull {
            serde_attrs.push("skip_serializing_if = \"Option::is_none\"".to_string());
        }
        if !serde_attrs.is_empty() {
            body.push_str(&format!("    #[serde({})]\n", serde_attrs.join(", ")));
        }
        body.push_str(&format!("    pub {rust_name}: {rendered},\n"));
    }
    for generated in &member_plan.generated {
        if generated.rust_name == "additional_properties" {
            body.push_str("    #[serde(default, flatten)]\n");
            body.push_str("    pub additional_properties: serde_json::Map<String, JsonValue>,\n");
        } else {
            return Err(SchemaToolError::msg(format!(
                "internal renderer error: unknown generated member `{}` for {type_name}",
                generated.rust_name
            ))
            .into());
        }
    }
    body.push_str("}\n");
    Ok(body)
}

fn render_tagged_union(
    type_name: &str,
    schema_id: &str,
    discriminator: &str,
    variants: &[ProjectedVariant],
    names: &BTreeMap<RustDeclarationId, String>,
) -> Result<String> {
    let mut body = format!(
        "/// Generated internally tagged union from `{schema_id}`\n#[derive(Debug, Clone, PartialEq, Serialize, Deserialize)]\n#[serde(tag = {discriminator:?}, deny_unknown_fields)]\npub enum {type_name} {{\n"
    );
    for variant in variants {
        body.push_str(&format!(
            "    #[serde(rename = {:?})]\n    {} {{\n",
            variant.tag, variant.rust_name
        ));
        for field in &variant.fields {
            let rust_name = &field.rust_name;
            let mut rendered =
                render_rust_type_expr(&field.ty, names, TypeRenderContext::TypesModule)?;
            if field.presence == Presence::Optional && !rendered.starts_with("Option<") {
                rendered = format!("Option<{rendered}>");
            }
            let mut serde_attrs = Vec::new();
            if rust_name != &field.json_name {
                serde_attrs.push(format!("rename = {:?}", field.json_name));
            }
            if field.presence == Presence::Optional && field.nullability == Nullability::NonNull {
                serde_attrs.push("skip_serializing_if = \"Option::is_none\"".to_string());
            }
            if !serde_attrs.is_empty() {
                body.push_str(&format!("        #[serde({})]\n", serde_attrs.join(", ")));
            }
            body.push_str(&format!("        {rust_name}: {rendered},\n"));
        }
        body.push_str("    },\n");
    }
    body.push_str("}\n");
    Ok(body)
}

#[derive(Clone, Copy)]
enum TypeRenderContext {
    TypesModule,
    TypedModule,
}

fn render_rust_type_expr(
    ty: &RustTypeExpr,
    names: &BTreeMap<RustDeclarationId, String>,
    ctx: TypeRenderContext,
) -> Result<String> {
    match ty {
        RustTypeExpr::Scalar(scalar) => Ok(match scalar {
            ScalarKind::Null => "NullOnly".into(),
            ScalarKind::Boolean => "bool".into(),
            ScalarKind::Integer { constraints } => render_integer_type(*constraints),
            ScalarKind::Number => "f64".into(),
            ScalarKind::String => "String".into(),
        }),
        RustTypeExpr::AnyJson => Ok(match ctx {
            TypeRenderContext::TypesModule => "JsonValue".into(),
            TypeRenderContext::TypedModule => "Value".into(),
        }),
        RustTypeExpr::Array(items) => Ok(format!(
            "Vec<{}>",
            render_rust_type_expr(items, names, ctx)?
        )),
        RustTypeExpr::Nullable(inner) => {
            let rendered = render_rust_type_expr(inner, names, ctx)?;
            if rendered.starts_with("Option<") {
                Ok(rendered)
            } else {
                Ok(format!("Option<{rendered}>"))
            }
        }
        RustTypeExpr::Boxed(inner) => Ok(format!(
            "Box<{}>",
            render_rust_type_expr(inner, names, ctx)?
        )),
        RustTypeExpr::Named(id) => names.get(id).cloned().ok_or_else(|| {
            SchemaToolError::msg(format!(
                "internal renderer error: missing Rust symbol for {}",
                id.display()
            ))
            .into()
        }),
    }
}

fn render_integer_type(constraints: IntegerConstraints) -> String {
    let minimum = constraints.minimum.map(JsonInteger::as_i128);
    let maximum = constraints.maximum.map(JsonInteger::as_i128);
    match (minimum, maximum) {
        // `i64` is the compatibility-preserving default contract for historical
        // generated integers. Specialize only when the proven non-negative range
        // needs the unsigned 32-bit contract (as method payload versions do).
        (Some(minimum), Some(maximum))
            if minimum >= 0
                && maximum > i128::from(i32::MAX)
                && maximum <= i128::from(u32::MAX) =>
        {
            "u32".into()
        }
        (Some(minimum), Some(maximum))
            if minimum >= 0
                && maximum > i128::from(i64::MAX)
                && maximum <= i128::from(u64::MAX) =>
        {
            "u64".into()
        }
        // Preserve existing public bytes for absent, one-sided, and historical
        // bounded ranges that fit the established signed contract.
        _ => "i64".into(),
    }
}

fn render_const_type(name: &str, schema_id: &str, value: &ConstJson) -> String {
    match value {
        ConstJson::String { value: expected } => format!(
            "/// Generated string const from `{schema_id}`\n#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash, Serialize, Deserialize)]\npub enum {name} {{\n    #[serde(rename = {:?})]\n    Value,\n}}\n",
            expected
        ),
        ConstJson::Integer { value: expected } => format!(
            "/// Generated integer const from `{schema_id}`\n#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash, Default)]\npub struct {name};\nimpl Serialize for {name} {{ fn serialize<S>(&self, serializer: S) -> Result<S::Ok, S::Error> where S: Serializer {{ serializer.serialize_i64({expected}) }} }}\nimpl<'de> Deserialize<'de> for {name} {{ fn deserialize<D>(deserializer: D) -> Result<Self, D::Error> where D: Deserializer<'de> {{ let value = i64::deserialize(deserializer)?; if value == {expected} {{ Ok(Self) }} else {{ Err(D::Error::custom(format!(\"expected integer const {expected}, got {{value}}\"))) }} }} }}\n"
        ),
        ConstJson::UnsignedInteger { value: expected } => format!(
            "/// Generated integer const from `{schema_id}`\n#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash, Default)]\npub struct {name};\nimpl Serialize for {name} {{ fn serialize<S>(&self, serializer: S) -> Result<S::Ok, S::Error> where S: Serializer {{ serializer.serialize_u64({expected}) }} }}\nimpl<'de> Deserialize<'de> for {name} {{ fn deserialize<D>(deserializer: D) -> Result<Self, D::Error> where D: Deserializer<'de> {{ let value = u64::deserialize(deserializer)?; if value == {expected} {{ Ok(Self) }} else {{ Err(D::Error::custom(format!(\"expected integer const {expected}, got {{value}}\"))) }} }} }}\n"
        ),
        ConstJson::Bool { value: expected } => format!(
            "/// Generated boolean const from `{schema_id}`\n#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash, Default)]\npub struct {name};\nimpl Serialize for {name} {{ fn serialize<S>(&self, serializer: S) -> Result<S::Ok, S::Error> where S: Serializer {{ serializer.serialize_bool({expected}) }} }}\nimpl<'de> Deserialize<'de> for {name} {{ fn deserialize<D>(deserializer: D) -> Result<Self, D::Error> where D: Deserializer<'de> {{ let value = bool::deserialize(deserializer)?; if value == {expected} {{ Ok(Self) }} else {{ Err(D::Error::custom(format!(\"expected boolean const {expected}, got {{value}}\"))) }} }} }}\n"
        ),
        ConstJson::Null => render_null_only().replace("NullOnly", name),
    }
}

fn render_string_enum(type_name: &str, schema_id: &str, values: &[String]) -> String {
    // One ordered mapping drives variants, ALL, and as_str so declaration order is shared.
    let mapping: Vec<(&str, String)> = values
        .iter()
        .map(|value| (value.as_str(), rust_enum_variant(value)))
        .collect();

    let mut body = format!(
        "/// Generated string enum from `{schema_id}`\n#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash, Serialize, Deserialize)]\npub enum {type_name} {{\n"
    );
    for (wire, variant) in &mapping {
        body.push_str(&format!(
            "    #[serde(rename = {wire:?})]\n    {variant},\n"
        ));
    }
    body.push_str("}\n");

    body.push_str(&format!("\nimpl {type_name} {{\n"));
    body.push_str(
        "    /// Schema enum declaration-order closed set (null filtered at use-site Option).\n",
    );
    body.push_str("    pub const ALL: &'static [Self] = &[\n");
    for (_, variant) in &mapping {
        body.push_str(&format!("        Self::{variant},\n"));
    }
    body.push_str("    ];\n\n");
    body.push_str("    pub fn as_str(self) -> &'static str {\n        match self {\n");
    for (wire, variant) in &mapping {
        body.push_str(&format!("            Self::{variant} => {wire:?},\n"));
    }
    body.push_str("        }\n    }\n}\n");
    body
}

