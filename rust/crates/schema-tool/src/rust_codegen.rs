//! Rust renderer that consumes a language-neutral [`TargetContractGraph`].
//!
//! All Rust naming, keywords, type mapping, serde attributes, variant names,
//! `include_str!` paths, and output paths are decided here. This module must
//! not re-read Schema shape keywords or `serde_json::Value` Schema documents.
//!
//! ## Identity separation
//!
//! - [`ContractTypeId`] is the canonical graph node (`$id` + JSON Pointer).
//! - [`RustDeclarationId`] is a projected declaration that may clone a single
//!   graph node under a use-site lineage (HEAD preserves
//!   `PolicyRuleCreatedBy` / `PolicyRuleUpdatedBy` as distinct declarations for
//!   one `/$defs/actorEntryPoint` node).
//!
//! Whole-schema `$ref` roots are shared (`SharedRoot`). Fragment / inline named
//! shapes used from a property path are projected as
//! `NominalInstantiation { canonical, use_site_lineage }`. The Rust symbol name
//! is stored independently on the projected declaration.
//!
//! ## Recursive layout
//!
//! After projection, a direct-value dependency graph is built (Named direct;
//! Nullable / field Optional direct; Array indirect). Deterministic SCCs wrap
//! same-component direct `Named` edges in [`RustTypeExpr::Boxed`]. Arrays do
//! not need `Box`.

