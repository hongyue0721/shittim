/// Rust declaration identity. Distinct from [`ContractTypeId`].
#[derive(Debug, Clone, PartialEq, Eq, PartialOrd, Ord, Hash)]
pub enum RustDeclarationId {
    /// One shared declaration for a whole-schema root ref.
    SharedRoot { schema_id: String },
    /// Named projection of a graph node under a use-site lineage.
    ///
    /// `use_site_lineage` is the chain of schema use-sites from the owning root
    /// projection down to this declaration. The Rust symbol name is **not** part
    /// of the identity; it lives on [`ProjectedDecl::rust_name`].
    NominalInstantiation {
        canonical: ContractTypeId,
        use_site_lineage: Vec<SourceUseSite>,
    },
}

impl RustDeclarationId {
    fn display(&self) -> String {
        match self {
            Self::SharedRoot { schema_id } => format!("shared:{schema_id}"),
            Self::NominalInstantiation {
                canonical,
                use_site_lineage,
            } => {
                let sites: Vec<String> = use_site_lineage
                    .iter()
                    .map(SourceUseSite::display)
                    .collect();
                format!("nominal:{}@{}", sites.join(" > "), canonical.display())
            }
        }
    }
}

impl fmt::Display for RustDeclarationId {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        f.write_str(&self.display())
    }
}

/// Projected Rust type expression (renderer-local; never stored in the graph).
#[derive(Debug, Clone, PartialEq, Eq)]
enum RustTypeExpr {
    Scalar(ScalarKind),
    AnyJson,
    Array(Box<RustTypeExpr>),
    Nullable(Box<RustTypeExpr>),
    Named(RustDeclarationId),
    /// Direct recursive ownership within an SCC (inserted by layout pass).
    Boxed(Box<RustTypeExpr>),
}

/// One projected declaration body.
#[derive(Debug, Clone)]
enum ProjectedShape {
    Object {
        fields: Vec<ProjectedField>,
        unknown_field_policy: UnknownFieldPolicy,
    },
    TaggedUnion {
        discriminator: String,
        variants: Vec<ProjectedVariant>,
    },
    StringEnum {
        values: Vec<String>,
    },
    Const {
        value: ConstJson,
    },
}

#[derive(Debug, Clone)]
struct ProjectedField {
    json_name: String,
    ty: RustTypeExpr,
    presence: Presence,
    nullability: Nullability,
}

#[derive(Debug, Clone)]
struct ProjectedVariant {
    tag: String,
    object_type_id: ContractTypeId,
    source: SourceUseSite,
    rust_name: String,
    fields: Vec<ProjectedField>,
}

#[derive(Debug, Clone)]
struct ProjectedDecl {
    id: RustDeclarationId,
    /// Source schema `$id` for doc comments (HEAD uses the owning schema id).
    doc_schema_id: String,
    rust_name: String,
    shape: ProjectedShape,
}

/// Single computed Rust projection for a [`TargetContractGraph`].
///
/// `render_types_module_from_projection` and `render_typed_module_from_projection`
/// must consume the **same** instance so declaration names and SCC layout stay
/// bijective across artifacts. Catalog does not need projection and may render
/// directly from the graph.
#[derive(Debug, Clone)]
pub struct RustProjection {
    decls: Vec<ProjectedDecl>,
    names: BTreeMap<RustDeclarationId, String>,
    /// SharedRoot schema_id -> rust name (payload / envelope root lookup).
    root_names: BTreeMap<String, String>,
}

impl RustProjection {
    /// Declarations in stable emission order (by rust_name).
    pub fn declarations(&self) -> impl Iterator<Item = (&RustDeclarationId, &str)> + '_ {
        self.decls.iter().map(|d| (&d.id, d.rust_name.as_str()))
    }

    /// Rust symbol for a declaration identity, if present.
    pub fn name_of(&self, id: &RustDeclarationId) -> Option<&str> {
        self.names.get(id).map(String::as_str)
    }

    /// Rust symbol for a whole-schema SharedRoot, if projected.
    pub fn root_name(&self, schema_id: &str) -> Option<&str> {
        self.root_names.get(schema_id).map(String::as_str)
    }

    /// All projected declaration identities for a canonical graph node.
    pub fn decls_for_canonical(&self, canonical: &ContractTypeId) -> Vec<&RustDeclarationId> {
        self.decls
            .iter()
            .filter_map(|decl| match &decl.id {
                RustDeclarationId::SharedRoot { schema_id }
                    if schema_id == &canonical.schema_id && canonical.is_root() =>
                {
                    Some(&decl.id)
                }
                RustDeclarationId::NominalInstantiation {
                    canonical: node, ..
                } if node == canonical => Some(&decl.id),
                _ => None,
            })
            .collect()
    }

    /// Count of NominalInstantiation declarations for `canonical`.
    pub fn nominal_count_for(&self, canonical: &ContractTypeId) -> usize {
        self.decls
            .iter()
            .filter(|decl| {
                matches!(
                    &decl.id,
                    RustDeclarationId::NominalInstantiation {
                        canonical: node,
                        ..
                    } if node == canonical
                )
            })
            .count()
    }

    /// Field type expression rendered for TypesModule context (inspection helper).
    pub fn field_type_expr(&self, decl_rust_name: &str, json_field: &str) -> Result<String> {
        let decl = self
            .decls
            .iter()
            .find(|d| d.rust_name == decl_rust_name)
            .ok_or_else(|| {
                SchemaToolError::msg(format!(
                    "projection has no declaration named {decl_rust_name}"
                ))
            })?;
        let ProjectedShape::Object { fields, .. } = &decl.shape else {
            return Err(SchemaToolError::msg(format!(
                "declaration {decl_rust_name} is not an object"
            ))
            .into());
        };
        let field = fields
            .iter()
            .find(|f| f.json_name == json_field)
            .ok_or_else(|| {
                SchemaToolError::msg(format!(
                    "declaration {decl_rust_name} has no field {json_field}"
                ))
            })?;
        let mut rendered =
            render_rust_type_expr(&field.ty, &self.names, TypeRenderContext::TypesModule)?;
        if field.presence == Presence::Optional && !rendered.starts_with("Option<") {
            rendered = format!("Option<{rendered}>");
        }
        Ok(rendered)
    }
}

/// Project the graph exactly once into a reusable [`RustProjection`].
pub fn project_rust(graph: &TargetContractGraph) -> Result<RustProjection> {
    let decls = Projector::new(graph).project_all()?;
    let names: BTreeMap<RustDeclarationId, String> = decls
        .iter()
        .map(|d| (d.id.clone(), d.rust_name.clone()))
        .collect();
    let root_names: BTreeMap<String, String> = decls
        .iter()
        .filter_map(|d| match &d.id {
            RustDeclarationId::SharedRoot { schema_id } => {
                Some((schema_id.clone(), d.rust_name.clone()))
            }
            _ => None,
        })
        .collect();
    Ok(RustProjection {
        decls,
        names,
        root_names,
    })
}

// ---------------------------------------------------------------------------
// Projection engine
// ---------------------------------------------------------------------------

struct Projector<'a> {
    graph: &'a TargetContractGraph,
    decls: BTreeMap<RustDeclarationId, ProjectedDecl>,
    /// Declarations currently being projected (cycle termination by decl id).
    projecting: BTreeSet<RustDeclarationId>,
    /// Canonical currently on the projection stack -> active decl (backedge reuse).
    /// Prevents infinite nominal lineage growth on recursive shapes.
    active_by_canonical: BTreeMap<ContractTypeId, RustDeclarationId>,
}

impl<'a> Projector<'a> {
    fn new(graph: &'a TargetContractGraph) -> Self {
        Self {
            graph,
            decls: BTreeMap::new(),
            projecting: BTreeSet::new(),
            active_by_canonical: BTreeMap::new(),
        }
    }

    fn project_all(mut self) -> Result<Vec<ProjectedDecl>> {
        // Project every root schema as a shared root declaration.
        for schema_id in &self.graph.source_schema_ids {
            let root = ContractTypeId::root(schema_id);
            if self.graph.nodes.contains_key(&root) {
                self.ensure_shared_root(&root)?;
            }
        }
        // Recursive layout: Box direct edges inside SCCs.
        apply_recursive_layout(&mut self.decls)?;

        // Stable order by rust_name for emission (matches HEAD BTreeMap-by-name).
        let mut out: Vec<_> = self.decls.into_values().collect();
        out.sort_by(|a, b| a.rust_name.cmp(&b.rust_name));
        // Fail closed on symbol collisions.
        let mut used: BTreeMap<String, RustDeclarationId> = BTreeMap::new();
        for decl in &out {
            if let Some(previous) = used.get(&decl.rust_name) {
                if previous != &decl.id {
                    return Err(SchemaToolError::msg(format!(
                        "Rust type name collision: `{}` is required by both {} and {} (canonical/use-site identities)",
                        decl.rust_name,
                        previous.display(),
                        decl.id.display()
                    ))
                    .into());
                }
            }
            used.insert(decl.rust_name.clone(), decl.id.clone());
            audit_decl_internal_collisions(decl)?;
        }
        Ok(out)
    }

    fn ensure_shared_root(&mut self, root: &ContractTypeId) -> Result<RustDeclarationId> {
        let id = RustDeclarationId::SharedRoot {
            schema_id: root.schema_id.clone(),
        };
        if self.decls.contains_key(&id) || !self.projecting.insert(id.clone()) {
            return Ok(id);
        }
        self.active_by_canonical.insert(root.clone(), id.clone());
        let node = self.graph.nodes.get(root).ok_or_else(|| {
            SchemaToolError::msg(format!(
                "internal renderer error: missing graph node {}",
                root.display()
            ))
        })?;
        let rust_name = root_rust_name(node);
        let shape = self.project_shape(&node.shape, root, &rust_name, &[], &root.schema_id)?;
        self.projecting.remove(&id);
        self.active_by_canonical.remove(root);
        self.decls.insert(
            id.clone(),
            ProjectedDecl {
                id: id.clone(),
                doc_schema_id: root.schema_id.clone(),
                rust_name,
                shape,
            },
        );
        Ok(id)
    }

    fn ensure_nominal(
        &mut self,
        canonical: &ContractTypeId,
        rust_name: &str,
        use_site_lineage: &[SourceUseSite],
        doc_schema_id: &str,
    ) -> Result<RustDeclarationId> {
        // Backedge reuse: if this canonical is already on the projection stack,
        // return the active declaration instead of growing lineage forever.
        if let Some(active) = self.active_by_canonical.get(canonical) {
            return Ok(active.clone());
        }

        let id = RustDeclarationId::NominalInstantiation {
            canonical: canonical.clone(),
            use_site_lineage: use_site_lineage.to_vec(),
        };
        if self.decls.contains_key(&id) || !self.projecting.insert(id.clone()) {
            return Ok(id);
        }
        self.active_by_canonical
            .insert(canonical.clone(), id.clone());
        let node = self.graph.nodes.get(canonical).ok_or_else(|| {
            SchemaToolError::msg(format!(
                "internal renderer error: missing graph node {} for name {rust_name} (use-sites: {})",
                canonical.display(),
                use_site_lineage
                    .iter()
                    .map(SourceUseSite::display)
                    .collect::<Vec<_>>()
                    .join(" > ")
            ))
        })?;
        let shape = self.project_shape(
            &node.shape,
            canonical,
            rust_name,
            use_site_lineage,
            doc_schema_id,
        )?;
        self.projecting.remove(&id);
        self.active_by_canonical.remove(canonical);
        self.decls.insert(
            id.clone(),
            ProjectedDecl {
                id: id.clone(),
                doc_schema_id: doc_schema_id.to_string(),
                rust_name: rust_name.to_string(),
                shape,
            },
        );
        Ok(id)
    }

    fn project_shape(
        &mut self,
        shape: &TypeShape,
        canonical: &ContractTypeId,
        rust_name: &str,
        use_site_lineage: &[SourceUseSite],
        doc_schema_id: &str,
    ) -> Result<ProjectedShape> {
        match shape {
            TypeShape::Object {
                fields,
                unknown_field_policy,
            } => {
                let mut projected = Vec::new();
                for field in fields {
                    let field_name = format!("{rust_name}{}", to_pascal_case(&field.json_name));
                    let child_lineage = {
                        let mut lineage = use_site_lineage.to_vec();
                        lineage.push(field.ty.source.clone());
                        lineage
                    };
                    let ty = self.project_type_use(
                        &field.ty,
                        &field_name,
                        &child_lineage,
                        doc_schema_id,
                    )?;
                    projected.push(ProjectedField {
                        json_name: field.json_name.clone(),
                        ty,
                        presence: field.presence,
                        nullability: field.nullability,
                    });
                }
                Ok(ProjectedShape::Object {
                    fields: projected,
                    unknown_field_policy: *unknown_field_policy,
                })
            }
            TypeShape::TaggedUnion {
                discriminator,
                branches,
                unknown_field_policy,
            } => {
                if *unknown_field_policy != UnknownFieldPolicy::Forbid {
                    return Err(SchemaToolError::msg(format!(
                        "unsupported tagged-union unknown-field policy at {}",
                        canonical.display()
                    ))
                    .into());
                }
                let mut variants = Vec::new();
                for branch in branches {
                    let branch_node =
                        self.graph
                            .nodes
                            .get(&branch.object_type_id)
                            .ok_or_else(|| {
                                SchemaToolError::msg(format!(
                                    "missing tagged-union branch {} at {} (arm {})",
                                    branch.tag,
                                    branch.object_type_id.display(),
                                    branch.source.display()
                                ))
                            })?;
                    let TypeShape::Object { fields, .. } = &branch_node.shape else {
                        return Err(SchemaToolError::msg(format!(
                            "tagged-union branch {} at {} (arm {}) is not an object",
                            branch.tag,
                            branch.object_type_id.display(),
                            branch.source.display()
                        ))
                        .into());
                    };
                    let variant_name = rust_enum_variant(&branch.tag);
                    let mut projected = Vec::new();
                    for field in fields
                        .iter()
                        .filter(|field| field.json_name != *discriminator)
                    {
                        let field_name = format!(
                            "{rust_name}{variant_name}{}",
                            to_pascal_case(&field.json_name)
                        );
                        let mut lineage = use_site_lineage.to_vec();
                        lineage.push(field.ty.source.clone());
                        projected.push(ProjectedField {
                            json_name: field.json_name.clone(),
                            ty: self.project_type_use(
                                &field.ty,
                                &field_name,
                                &lineage,
                                doc_schema_id,
                            )?,
                            presence: field.presence,
                            nullability: field.nullability,
                        });
                    }
                    variants.push(ProjectedVariant {
                        tag: branch.tag.clone(),
                        object_type_id: branch.object_type_id.clone(),
                        source: branch.source.clone(),
                        rust_name: variant_name,
                        fields: projected,
                    });
                }
                Ok(ProjectedShape::TaggedUnion {
                    discriminator: discriminator.clone(),
                    variants,
                })
            }
            TypeShape::StringEnum { values } => Ok(ProjectedShape::StringEnum {
                values: values.clone(),
            }),
            TypeShape::Const { value } => Ok(ProjectedShape::Const {
                value: value.clone(),
            }),
            // Named declarations are only emitted for Object / StringEnum / Const
            // (matching HEAD). Scalar/Any/Array/Nullable shapes are inlined at use sites
            // and must not become standalone declarations.
            TypeShape::Scalar { .. }
            | TypeShape::AnyJson
            | TypeShape::Array { .. }
            | TypeShape::Nullable { .. } => Err(SchemaToolError::msg(format!(
                "internal renderer error: cannot project non-named shape at {} (name {rust_name})",
                canonical.display()
            ))
            .into()),
        }
    }

    fn project_type_use(
        &mut self,
        ty: &TypeUse,
        rust_name: &str,
        use_site_lineage: &[SourceUseSite],
        doc_schema_id: &str,
    ) -> Result<RustTypeExpr> {
        match &ty.expr {
            TypeExpr::Scalar { scalar } => Ok(RustTypeExpr::Scalar(*scalar)),
            TypeExpr::AnyJson => Ok(RustTypeExpr::AnyJson),
            TypeExpr::Array { items } => {
                let item_name = format!("{rust_name}Item");
                let mut item_lineage = use_site_lineage.to_vec();
                // Array items keep the array use-site; item schema source is on items.source.
                item_lineage.push(items.source.clone());
                Ok(RustTypeExpr::Array(Box::new(self.project_type_use(
                    items,
                    &item_name,
                    &item_lineage,
                    doc_schema_id,
                )?)))
            }
            TypeExpr::Nullable { inner } => Ok(RustTypeExpr::Nullable(Box::new(
                self.project_type_use(inner, rust_name, use_site_lineage, doc_schema_id)?,
            ))),
            TypeExpr::Reference { id } => {
                self.project_reference(id, rust_name, use_site_lineage, doc_schema_id)
            }
        }
    }

    fn project_reference(
        &mut self,
        id: &ContractTypeId,
        rust_name: &str,
        use_site_lineage: &[SourceUseSite],
        doc_schema_id: &str,
    ) -> Result<RustTypeExpr> {
        let node = self.graph.nodes.get(id).ok_or_else(|| {
            SchemaToolError::msg(format!(
                "internal renderer error: missing referenced node {} (name {rust_name}, use-sites {})",
                id.display(),
                use_site_lineage
                    .iter()
                    .map(SourceUseSite::display)
                    .collect::<Vec<_>>()
                    .join(" > ")
            ))
        })?;

        // Whole-schema roots are always shared (one declaration per schema_id).
        if id.is_root() {
            let decl = self.ensure_shared_root(id)?;
            return Ok(RustTypeExpr::Named(decl));
        }

        // Named shapes under a fragment / inline pointer: clone per use-site lineage
        // so shared $defs become PolicyRuleCreatedBy / PolicyRuleUpdatedBy.
        if shape_is_named(&node.shape) {
            let decl = self.ensure_nominal(id, rust_name, use_site_lineage, doc_schema_id)?;
            return Ok(RustTypeExpr::Named(decl));
        }

        // Non-named shapes referenced by identity: project inline.
        match &node.shape {
            TypeShape::Scalar { scalar } => Ok(RustTypeExpr::Scalar(*scalar)),
            TypeShape::AnyJson => Ok(RustTypeExpr::AnyJson),
            TypeShape::Array { items } => {
                let item_name = format!("{rust_name}Item");
                let mut item_lineage = use_site_lineage.to_vec();
                item_lineage.push(items.source.clone());
                Ok(RustTypeExpr::Array(Box::new(self.project_type_use(
                    items,
                    &item_name,
                    &item_lineage,
                    doc_schema_id,
                )?)))
            }
            TypeShape::Nullable { inner } => Ok(RustTypeExpr::Nullable(Box::new(
                self.project_type_use(inner, rust_name, use_site_lineage, doc_schema_id)?,
            ))),
            TypeShape::Object { .. }
            | TypeShape::TaggedUnion { .. }
            | TypeShape::StringEnum { .. }
            | TypeShape::Const { .. } => Err(SchemaToolError::msg(format!(
                "internal renderer error: named shape for {} fell through without declaration",
                id.display()
            ))
            .into()),
        }
    }
}

// ---------------------------------------------------------------------------
// Recursive layout (direct-value SCC + Box)
// ---------------------------------------------------------------------------

fn apply_recursive_layout(decls: &mut BTreeMap<RustDeclarationId, ProjectedDecl>) -> Result<()> {
    // Direct-value dependency edges: Named is direct; Nullable unwraps as direct;
    // Array is indirect (no edge). Field presence Optional is still direct ownership.
    let mut edges: BTreeMap<RustDeclarationId, BTreeSet<RustDeclarationId>> = BTreeMap::new();
    for id in decls.keys() {
        edges.entry(id.clone()).or_default();
    }
    for (id, decl) in decls.iter() {
        match &decl.shape {
            ProjectedShape::Object { fields, .. } => {
                for field in fields {
                    for dep in direct_named_deps(&field.ty) {
                        edges.entry(id.clone()).or_default().insert(dep);
                    }
                }
            }
            ProjectedShape::TaggedUnion { variants, .. } => {
                for variant in variants {
                    for field in &variant.fields {
                        for dep in direct_named_deps(&field.ty) {
                            edges.entry(id.clone()).or_default().insert(dep);
                        }
                    }
                }
            }
            ProjectedShape::StringEnum { .. } | ProjectedShape::Const { .. } => {}
        }
    }

    let scc_of = deterministic_sccs(&edges);
    for (id, decl) in decls.iter_mut() {
        let owner_scc = scc_of.get(id).copied().unwrap_or(0);
        match &mut decl.shape {
            ProjectedShape::Object { fields, .. } => {
                for field in fields {
                    box_direct_scc_named(&mut field.ty, owner_scc, &scc_of);
                }
            }
            ProjectedShape::TaggedUnion { variants, .. } => {
                for variant in variants {
                    for field in &mut variant.fields {
                        box_direct_scc_named(&mut field.ty, owner_scc, &scc_of);
                    }
                }
            }
            ProjectedShape::StringEnum { .. } | ProjectedShape::Const { .. } => {}
        }
    }
    Ok(())
}

fn direct_named_deps(ty: &RustTypeExpr) -> Vec<RustDeclarationId> {
    match ty {
        RustTypeExpr::Named(id) => vec![id.clone()],
        RustTypeExpr::Nullable(inner) | RustTypeExpr::Boxed(inner) => direct_named_deps(inner),
        // Array is indirect ownership — no direct edge.
        RustTypeExpr::Array(_) => Vec::new(),
        RustTypeExpr::Scalar(_) | RustTypeExpr::AnyJson => Vec::new(),
    }
}

fn box_direct_scc_named(
    ty: &mut RustTypeExpr,
    owner_scc: usize,
    scc_of: &BTreeMap<RustDeclarationId, usize>,
) {
    match ty {
        RustTypeExpr::Named(id) => {
            if scc_of.get(id).copied() == Some(owner_scc) {
                let named = RustTypeExpr::Named(id.clone());
                *ty = RustTypeExpr::Boxed(Box::new(named));
            }
        }
        RustTypeExpr::Nullable(inner) | RustTypeExpr::Boxed(inner) => {
            box_direct_scc_named(inner, owner_scc, scc_of);
        }
        // Do not insert Box through Array indirection.
        RustTypeExpr::Array(_) => {}
        RustTypeExpr::Scalar(_) | RustTypeExpr::AnyJson => {}
    }
}

/// Deterministic Kosaraju SCC. Component ids are assigned in sorted-root order.
fn deterministic_sccs(
    edges: &BTreeMap<RustDeclarationId, BTreeSet<RustDeclarationId>>,
) -> BTreeMap<RustDeclarationId, usize> {
    let mut nodes: Vec<RustDeclarationId> = edges.keys().cloned().collect();
    nodes.sort();

    // First pass: order by finish time (iterative DFS, nodes visited in sorted order).
    let mut visited = BTreeSet::new();
    let mut order = Vec::new();
    for start in &nodes {
        if !visited.insert(start.clone()) {
            continue;
        }
        let mut stack = vec![(start.clone(), false)];
        while let Some((node, post)) = stack.pop() {
            if post {
                order.push(node);
                continue;
            }
            stack.push((node.clone(), true));
            if let Some(succs) = edges.get(&node) {
                let mut succ_list: Vec<_> = succs.iter().cloned().collect();
                succ_list.sort();
                // Push reverse so smallest is processed first.
                for succ in succ_list.into_iter().rev() {
                    if visited.insert(succ.clone()) {
                        stack.push((succ, false));
                    }
                }
            }
        }
    }

    // Transpose.
    let mut rev: BTreeMap<RustDeclarationId, BTreeSet<RustDeclarationId>> = BTreeMap::new();
    for node in &nodes {
        rev.entry(node.clone()).or_default();
    }
    for (from, tos) in edges {
        for to in tos {
            rev.entry(to.clone()).or_default().insert(from.clone());
        }
    }

    let mut scc_of = BTreeMap::new();
    let mut assigned = BTreeSet::new();
    let mut component_id = 0usize;
    for start in order.into_iter().rev() {
        if !assigned.insert(start.clone()) {
            continue;
        }
        let mut stack = vec![start.clone()];
        scc_of.insert(start.clone(), component_id);
        while let Some(node) = stack.pop() {
            if let Some(preds) = rev.get(&node) {
                let mut pred_list: Vec<_> = preds.iter().cloned().collect();
                pred_list.sort();
                for pred in pred_list {
                    if assigned.insert(pred.clone()) {
                        scc_of.insert(pred.clone(), component_id);
                        stack.push(pred);
                    }
                }
            }
        }
        component_id += 1;
    }
    scc_of
}

fn shape_is_named(shape: &TypeShape) -> bool {
    matches!(
        shape,
        TypeShape::Object { .. }
            | TypeShape::TaggedUnion { .. }
            | TypeShape::StringEnum { .. }
            | TypeShape::Const { .. }
    )
}

fn root_rust_name(node: &crate::contract_model::ContractTypeNode) -> String {
    if let Some(title) = &node.schema_title {
        if !title.is_empty() {
            return to_pascal_case(title);
        }
    }
    type_name_from_schema_id(&node.id.schema_id)
}

fn type_name_from_schema_id(id: &str) -> String {
    to_pascal_case(
        id.rsplit('/')
            .next()
            .unwrap_or(id)
            .trim_end_matches(".json"),
    )
}

fn audit_decl_internal_collisions(decl: &ProjectedDecl) -> Result<()> {
    match &decl.shape {
        ProjectedShape::Object { fields, .. } => {
            let mut rust_fields: BTreeMap<String, String> = BTreeMap::new();
            for field in fields {
                let rust_name = rust_field_name(&field.json_name);
                if let Some(previous) =
                    rust_fields.insert(rust_name.clone(), field.json_name.clone())
                {
                    return Err(SchemaToolError::msg(format!(
                        "Rust field name collision in `{}` ({}): json fields {previous:?} and {:?} both map to `{rust_name}` (use-sites /properties/{previous} and /properties/{})",
                        decl.rust_name,
                        decl.id.display(),
                        field.json_name,
                        field.json_name
                    ))
                    .into());
                }
            }
        }
        ProjectedShape::TaggedUnion { variants, .. } => {
            let mut names = BTreeMap::new();
            for variant in variants {
                if let Some(previous) = names.insert(variant.rust_name.clone(), variant.tag.clone())
                {
                    return Err(SchemaToolError::msg(format!(
                        "Rust tagged-union variant collision in `{}` ({}): tags {previous:?} (canonical {}, arm {}) and {:?} (canonical {}, arm {}) both map to `{}`",
                        decl.rust_name,
                        decl.id.display(),
                        variants
                            .iter()
                            .find(|candidate| candidate.tag == previous)
                            .map(|candidate| candidate.object_type_id.display())
                            .unwrap_or_else(|| "<missing canonical identity>".to_string()),
                        variants
                            .iter()
                            .find(|candidate| candidate.tag == previous)
                            .map(|candidate| candidate.source.display())
                            .unwrap_or_else(|| "<missing arm>".to_string()),
                        variant.tag,
                        variant.object_type_id.display(),
                        variant.source.display(),
                        variant.rust_name
                    ))
                    .into());
                }
                let mut fields = BTreeMap::new();
                for field in &variant.fields {
                    let rust_name = rust_field_name(&field.json_name);
                    if let Some(previous) =
                        fields.insert(rust_name.clone(), field.json_name.clone())
                    {
                        return Err(SchemaToolError::msg(format!(
                            "Rust tagged-union field collision in `{}` variant `{}`: json fields {previous:?} and {:?} both map to `{rust_name}`",
                            decl.rust_name, variant.rust_name, field.json_name
                        ))
                        .into());
                    }
                }
            }
        }
        ProjectedShape::StringEnum { values } => {
            let mut variants: BTreeMap<String, String> = BTreeMap::new();
            for value in values {
                let variant = rust_enum_variant(value);
                if let Some(previous) = variants.insert(variant.clone(), value.clone()) {
                    return Err(SchemaToolError::msg(format!(
                        "Rust enum variant collision in type `{}` ({}): wire values {previous:?} and {value:?} both map to variant `{variant}`",
                        decl.rust_name,
                        decl.id.display()
                    ))
                    .into());
                }
            }
        }
        ProjectedShape::Const { .. } => {}
    }
    Ok(())
}

