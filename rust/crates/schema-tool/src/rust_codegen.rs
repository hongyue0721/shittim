// ---------------------------------------------------------------------------
// Typed envelope rendering (reuses graph root object fields)
// ---------------------------------------------------------------------------

fn render_typed_envelope(
    binding: &EnvelopeWireBinding,
    names: &BTreeMap<RustDeclarationId, String>,
    root_name: &BTreeMap<String, String>,
    decl_by_id: &BTreeMap<RustDeclarationId, &ProjectedDecl>,
    imports: &mut BTreeSet<String>,
) -> Result<String> {
    let base_name = typed_envelope_base_name(&binding.schema_title);
    let api_prefix = if binding.schema_title.starts_with("Kcp") {
        "Kcp"
    } else {
        ""
    };
    let payload_enum_name = format!("{api_prefix}{base_name}Payload");
    let typed_name = format!("Typed{api_prefix}{base_name}Envelope");
    let raw_name = format!("Raw{api_prefix}{base_name}Envelope");
    let schema_const_name = format!("{}_SCHEMA_ID", to_upper_snake_case(&binding.schema_title));

    let mut variant_to_value: BTreeMap<String, String> = BTreeMap::new();
    let mut mappings_rendered = Vec::new();
    for mapping in &binding.mappings {
        let variant_name = to_pascal_case(&mapping.discriminator_value);
        if let Some(previous) =
            variant_to_value.insert(variant_name.clone(), mapping.discriminator_value.clone())
        {
            return Err(SchemaToolError::msg(format!(
                "conditional payload mapping error in {}: Rust variant collision: discriminator values {previous:?} and {:?} both map to {variant_name}",
                binding.schema_id, mapping.discriminator_value
            ))
            .into());
        }
        let payload_type = root_name
            .get(&mapping.payload_type.schema_id)
            .cloned()
            .ok_or_else(|| {
                SchemaToolError::msg(format!(
                    "internal renderer error: missing payload type for {}",
                    mapping.payload_type.display()
                ))
            })?;
        imports.insert(payload_type.clone());
        mappings_rendered.push((
            mapping.discriminator_value.clone(),
            variant_name,
            payload_type,
        ));
    }

    // Consume the same projected layout as types.rs (no parallel envelope projection).
    let root_decl_id = RustDeclarationId::SharedRoot {
        schema_id: binding.schema_id.clone(),
    };
    let root_decl = decl_by_id.get(&root_decl_id).ok_or_else(|| {
        SchemaToolError::msg(format!(
            "internal renderer error: missing projected envelope root for {}",
            binding.schema_id
        ))
    })?;
    let ProjectedShape::Object { fields, .. } = &root_decl.shape else {
        return Err(SchemaToolError::msg(format!(
            "internal renderer error: envelope root is not an object projection: {}",
            binding.schema_id
        ))
        .into());
    };

    let mut ordered_fields: Vec<&ProjectedField> = fields.iter().collect();
    ordered_fields.sort_by(|a, b| a.json_name.cmp(&b.json_name));

    let mut raw_fields = Vec::new();
    let mut public_fields = Vec::new();
    let mut assignments = Vec::new();
    for field in ordered_fields {
        let json_name = &field.json_name;
        if json_name == &binding.discriminator {
            raw_fields.push(render_wire_field(json_name, "String", true));
            continue;
        }
        if json_name == "payload" {
            raw_fields.push(render_wire_field(json_name, "Value", true));
            continue;
        }
        let mut rust_type =
            render_rust_type_expr(&field.ty, names, TypeRenderContext::TypedModule)?;
        if field.presence == Presence::Optional && !rust_type.starts_with("Option<") {
            rust_type = format!("Option<{rust_type}>");
        }
        collect_named_type_imports(&rust_type, imports);
        raw_fields.push(render_wire_field(json_name, &rust_type, true));
        public_fields.push(render_wire_field(json_name, &rust_type, false));
        let field_rust = rust_field_name(json_name);
        assignments.push(format!("            {field_rust}: raw.{field_rust},\n"));
    }

    let mut out = String::new();
    out.push_str(&format!(
        "pub const {schema_const_name}: &str = {:?};\n\n",
        binding.schema_id
    ));
    out.push_str(&format!(
        "#[derive(Debug, Clone, PartialEq)]\npub struct {typed_name} {{\n"
    ));
    for field in &public_fields {
        out.push_str(field);
    }
    out.push_str(&format!(
        "    pub {}: String,\n",
        rust_field_name(&binding.discriminator)
    ));
    out.push_str(&format!("    pub payload: {payload_enum_name},\n}}\n\n"));

    out.push_str(&format!(
        "#[derive(Debug, Clone, PartialEq)]\npub enum {payload_enum_name} {{\n"
    ));
    for (_, variant_name, payload_type) in &mappings_rendered {
        out.push_str(&format!("    {variant_name}(Box<{payload_type}>),\n"));
    }
    out.push_str("}\n\n");

    out.push_str(&format!(
        "#[derive(Debug, Deserialize)]\n#[serde(deny_unknown_fields)]\nstruct {raw_name} {{\n"
    ));
    for field in &raw_fields {
        out.push_str(field);
    }
    out.push_str("}\n\n");

    let discriminator_field = rust_field_name(&binding.discriminator);
    out.push_str(&format!(
        "impl {typed_name} {{\n    pub fn decode(value: Value) -> Result<Self, ContractError> {{\n"
    ));
    out.push_str(&format!(
        "        validate_json({schema_const_name}, &value)?;\n        Self::decode_after_validation(value)\n    }}\n\n    /// Decodes a value that has already passed this envelope's generated Schema.\n    ///\n    /// Callers must validate with the matching envelope Schema before using this method.\n    pub fn decode_after_validation(value: Value) -> Result<Self, ContractError> {{\n"
    ));
    out.push_str(&format!(
        "        let raw: {raw_name} = deserialize_wire({schema_const_name}, value)?;\n"
    ));
    out.push_str(&format!(
        "        let discriminator = raw.{discriminator_field};\n"
    ));
    out.push_str("        let payload = match discriminator.as_str() {\n");
    for (disc_value, variant_name, _) in &mappings_rendered {
        out.push_str(&format!(
            "            {disc_value:?} => {payload_enum_name}::{variant_name}(Box::new(deserialize_payload({schema_const_name}, {disc_value:?}, raw.payload)?)),\n"
        ));
    }
    out.push_str(&format!("            value => return Err(ContractError::GeneratedDiscriminatorMapping {{ schema_id: {schema_const_name}.to_string(), discriminator: value.to_string() }}),\n"));
    out.push_str("        };\n        Ok(Self {\n");
    for assignment in assignments {
        out.push_str(&assignment);
    }
    out.push_str(&format!(
        "            {discriminator_field}: discriminator,\n            payload,\n        }})\n    }}\n}}\n"
    ));
    Ok(out)
}

fn render_wire_field(json_name: &str, rust_type: &str, private: bool) -> String {
    let rust_name = rust_field_name(json_name);
    let mut out = String::new();
    if rust_name != json_name {
        out.push_str(&format!("    #[serde(rename = {:?})]\n", json_name));
    }
    let visibility = if private { "" } else { "pub " };
    out.push_str(&format!("    {visibility}{rust_name}: {rust_type},\n"));
    out
}

fn collect_named_type_imports(rust_type: &str, imports: &mut BTreeSet<String>) {
    for token in rust_type.split(|ch: char| !ch.is_ascii_alphanumeric() && ch != '_') {
        if token
            .chars()
            .next()
            .is_some_and(|first| first.is_ascii_uppercase())
            && !matches!(token, "Option" | "String" | "Value" | "Box" | "Vec")
        {
            imports.insert(token.to_string());
        }
    }
}

fn typed_envelope_base_name(title: &str) -> String {
    let name = to_pascal_case(title);
    let without_prefix = name.strip_prefix("Kcp").unwrap_or(&name);
    without_prefix
        .strip_suffix("Envelope")
        .unwrap_or(without_prefix)
        .to_string()
}

fn render_typed_decode_helpers() -> &'static str {
    "fn deserialize_wire<T>(schema_id: &str, value: Value) -> Result<T, ContractError>\nwhere\n    T: for<'de> Deserialize<'de>,\n{\n    serde_json::from_value(value).map_err(|error| ContractError::WireDecodeAfterSchema {\n        schema_id: schema_id.to_string(),\n        detail: error.to_string(),\n    })\n}\n\nfn deserialize_payload<T>(\n    schema_id: &str,\n    discriminator: &str,\n    value: Value,\n) -> Result<T, ContractError>\nwhere\n    T: for<'de> Deserialize<'de>,\n{\n    serde_json::from_value(value).map_err(|error| ContractError::PayloadDecodeAfterSchema {\n        schema_id: schema_id.to_string(),\n        discriminator: discriminator.to_string(),\n        detail: error.to_string(),\n    })\n}\n"
}

// ---------------------------------------------------------------------------
// Rust naming policy (renderer exclusive)
// ---------------------------------------------------------------------------

fn rust_field_name(json_name: &str) -> String {
    let value = to_snake_case(json_name);
    if is_rust_keyword(&value) {
        format!("{value}_")
    } else {
        value
    }
}

fn rust_enum_variant(value: &str) -> String {
    if value.len() == 2 && value.starts_with('S') && value.as_bytes()[1].is_ascii_digit() {
        value.into()
    } else {
        to_pascal_case(value)
    }
}

fn is_rust_keyword(value: &str) -> bool {
    matches!(
        value,
        "as" | "async"
            | "await"
            | "break"
            | "const"
            | "continue"
            | "crate"
            | "dyn"
            | "else"
            | "enum"
            | "extern"
            | "false"
            | "fn"
            | "for"
            | "if"
            | "impl"
            | "in"
            | "let"
            | "loop"
            | "match"
            | "mod"
            | "move"
            | "mut"
            | "pub"
            | "ref"
            | "return"
            | "self"
            | "Self"
            | "static"
            | "struct"
            | "super"
            | "trait"
            | "true"
            | "type"
            | "unsafe"
            | "use"
            | "where"
            | "while"
    )
}

